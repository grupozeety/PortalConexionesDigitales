<div class='contenidoInterior'>
	<h1 class="centrar">Presentaci&oacute;n</h1>
	<p class="justificar">La información es una activo valioso que debe
		preservarse. Tener un riesgo alto en aspectos como la disponibiliad,
		confidencialidad e integridad de la misma, pueden llevar a que la
		institución, o el personal relacionado, tenga inconvenientes en
		elproceso de conseguir los objetivos misionales.</p>
	<p class="justificar">La Universidad Distrital Francisco José de
		Caldas, consciente del valor estratégico que tiene la información para
		el desarrollo de su misión, propone el presente seminario para
		constituir un ambiente en el que colectivamente se formen elementos
		conceptuales y procedimentales para el aseguramiento de activos de
		información. Así mismo, sirve como plataforma para la
		institucionalización del grupo de respuesta a incidentes IC-SIRT-UD,
		con el cual se tendrá un apoyo efectivo para enfrentar los riesgos
		relacionados con la seguridad de la información.</p>
	<h1 class="centrar">Objetivos</h1>
	<ul>
		<li>Elevar la consciencia institucional respecto al valor de la
			información.</li>
		<li>Consolidar un grupo de respuesta a incidentes de seguridad de la
			información de nivel institucional.</li>
		<li>Presentar, en un entorno controlado, algunas herramientas que
			comunmente se utilizan en el ámbito de la seguridad informática y la
			informática forense.</li>
		<li>Aumentar el nivel de compromiso de los miembros de la comunidad,
			en cuando a la construcción de un sistema de gestión de la seguridad
			de la información.</li>
	</ul>
</div>
